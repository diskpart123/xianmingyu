num1 = 10
num1 += 1  # 代表：num1=num1+1
print(num1)

num1 -= 3  # 代表：num1=num1-3
print(num1)

num1 *= 3  # 代表：num1=num1*3
print(num1)

num1 /= 2  # 代表：num1=num1/2   整数和整数相除的结果是实数,两个有一个是实数，结果总是实数
print(num1)

num1 //= 2  # 代表：num1=num1//2  整数和整数相除的结果是整数,两个有一个是实数，结果总是实数
print(num1)

num1 %= 5  # 代表：num1=num1%5
print(num1)

num1 **= 3  # 代表：num1=num1*num1*num1
print(num1)
